// We declare variables in JS using var, let and const
// var and let are mutable
// const are immutable

// Using let to declare variables
let names = "Margaret";
console.log(names);

let age = 22
console.log(age);

// Using const to declare variables
const school = "AkiraChix"
console.log(school);

const town = "Karen"
console.log(town);


// Using var to declare variables
var car = "Benz"
console.log(car);

var house = "mansion"
console.log(house);

// re-declaring variables
var house = "bungalow"
console.log(house);

var car = "TX";
console.log(car);


// undefined variable
let course;
console.log(course);

let number1 = 30;
let number2 = 40;
let sum = number1 + number2;
console.log(sum);





// Takeaways

// We use let, var and const to declare variables in JavaScript
// var and let are mutable, const is immutable
// When a variable is declared without passing a value to it, the output is undefined
// We can redeclare variables once we have assigned them values, using let and var keywords